public class Buttons {

	private String BUTTONS_ID;
	private String BUTTONS_NAME;
	private String BUTTONS_EVENT;
	private String BUTTONS_ORDER;
	private String BUTTONS_ICON;
	private String ENABLED;
	private String DESCRIPTIAN;
	
	public String getBUTTONS_ID() {
		return BUTTONS_ID;
	}
	public void setBUTTONS_ID(String bUTTONS_ID) {
		BUTTONS_ID = bUTTONS_ID;
	}
	public String getBUTTONS_NAME() {
		return BUTTONS_NAME;
	}
	public void setBUTTONS_NAME(String bUTTONS_NAME) {
		BUTTONS_NAME = bUTTONS_NAME;
	}
	public String getBUTTONS_EVENT() {
		return BUTTONS_EVENT;
	}
	public void setBUTTONS_EVENT(String bUTTONS_EVENT) {
		BUTTONS_EVENT = bUTTONS_EVENT;
	}
	public String getBUTTONS_ORDER() {
		return BUTTONS_ORDER;
	}
	public void setBUTTONS_ORDER(String bUTTONS_ORDER) {
		BUTTONS_ORDER = bUTTONS_ORDER;
	}
	public String getBUTTONS_ICON() {
		return BUTTONS_ICON;
	}
	public void setBUTTONS_ICON(String bUTTONS_ICON) {
		BUTTONS_ICON = bUTTONS_ICON;
	}
	public String getENABLED() {
		return ENABLED;
	}
	public void setENABLED(String eNABLED) {
		ENABLED = eNABLED;
	}
	public String getDESCRIPTIAN() {
		return DESCRIPTIAN;
	}
	public void setDESCRIPTIAN(String dESCRIPTIAN) {
		DESCRIPTIAN = dESCRIPTIAN;
	}
	
	
}
